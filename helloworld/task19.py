# Это желторотый питон по кличке "Каа". Возраст питомца: 19 лет. Имя владельца: Саша

def get_age_suffix(age):
    if age % 10 == 1 and age % 100 != 11:
        # 1 год, 21 год, 31 год, но 11 лет - исключение
        return 'год'
    elif 2 <= age % 10 <= 4 and (age % 100 < 10 or age % 100 >= 20):
        # 22 года, 34 года, 2 года, 4 года, но 12 лет, 14 лет - исключение
        return 'года'
    else:
        # 5 лет, 25 лет, 39 лет, остальные случаи
        return 'лет'

pets = dict()
pets_quantity = int(input("Введите количество питомцев : "))

for i in range(pets_quantity):
    pet_name_key = input("Введите имя питомца : ")
    pet_type = input("Введите вид питомца : ")
    pet_age = int(input("Введите возраст питомца : "))
    owner_name = input("Введите имя владельца : ")

    pets[pet_name_key] = {
        'Вид питомца': pet_type,
        'Возраст питомца': pet_age,
        'Имя владельца': owner_name
    }

for k in pets.keys():
    pet_information = pets[k]
    pet_type = pet_information['Вид питомца']
    pet_age = pet_information['Возраст питомца']
    owner_name = pet_information['Имя владельца']

    age_suffix = get_age_suffix(pet_age)
    output_string = f'Это {pet_type} по кличке "{k}". Возраст питомца: {pet_age} {age_suffix}. Имя владельца: {owner_name}.'
    print(output_string)